    private void botonProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonProductosActionPerformed
        if (opcionesPBox.getSelectedIndex() == -1 || codigoBoxProd.getSelectedIndex() == -1 || nuevoValor.getText().equals("")) {
            infoBox("Debe completar todos los campos correctamente", "Atencion");
        } else {
            String itemP = (String) opcionesPBox.getSelectedItem();
            String codigo = (String) codigoBoxProd.getSelectedItem();
            String query = "UPDATE producto SET " + itemP + " = " + nuevoValor.getText() + " WHERE codigo = " + codigo;
            try {
                tdao.editarAgregar(query);
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_botonProductosActionPerformed

