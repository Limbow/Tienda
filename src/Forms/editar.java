    private void setearCombosBox() {

        try {
            /*FABRICANTES*/
            opcionesFBox.addItem("codigo");
            opcionesFBox.addItem("nombre");
            for (int i = 1; i < tdao.obtenerCodigo("fabricante") + 1; i++) {
                codigoBoxFab.addItem(i + "");
            }

            /*PRODUCTOS*/
            opcionesPBox.addItem("codigo");
            opcionesPBox.addItem("nombre");
            opcionesPBox.addItem("precio");
            opcionesPBox.addItem("codigo fabricante");
            for (int i = 1; i < tdao.obtenerCodigo("producto") + 1; i++) {
                codigoBoxProd.addItem(i + "");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }
